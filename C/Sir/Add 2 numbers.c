//Program for adding two numbers
#include<stdio.h>
int main()
{
    int a,b;//variable declared
    a = 5;//value assigned to variable a
    b = -7;//value assigned to variable b
    int sum;//variable declared
    sum = a+b;//stored addition of a and b in sum variable
    printf("a+b = %d\n",sum);
    printf("a*b = %d\n",a*b);
    printf("%d + %d = %d\n",a,b,sum);
    printf("a-b = %d",a-b);


    return 0;
}
